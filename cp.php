function str($count)
{
    $var = '';
    for ($i = 1; $i < $count; $i++) {
        $var .= ' ';
    }

    return $var;
}

function crawlerProcessor(DOMNode $element, $level = 1)
{
    if (isset($element->tagName)) {
        echo str($level) . '<' . $element->tagName . '>' . PHP_EOL;
        foreach ($element->childNodes as $value) {
            //usleep(300000);
            crawlerProcessor($value, $level + 1);
        }
        echo str($level) . '</' . $element->tagName . '>' . PHP_EOL;
    } else {
        print_r($element->textContent);
    }
}

